package Lesson_02;

public class Topic_08_Bitwise_Operator_Number {
    public static void main(String[] args) {
        System.out.println("4 & 5: " + (4 & 5));
        // 100 = 0*2^0 + 0*2^1 + 0*2^2 = 4
        // 101 = 1*2^0 + 0*2^1 + 1*2^2 = 5
        //

        System.out.println("4 | 5: " + (4 | 5));
    }
}
